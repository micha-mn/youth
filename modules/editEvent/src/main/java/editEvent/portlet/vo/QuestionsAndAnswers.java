package editEvent.portlet.vo;

public class QuestionsAndAnswers {
	String id;
	String question;
	String questionAr;
	String answer;
	String answerAr;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getQuestion() {
		return question;
	}
	public String getQuestionAr() {
		return questionAr;
	}
	
	public void setQuestion(String question) {
		this.question = question;
	}
	public void setQuestionAr(String questionAr) {
		this.questionAr = questionAr;
	}
	
	public String getAnswer() {
		return answer;
	}
	public String getAnswerAr() {
		return answerAr;
	}
	
	public void setAnswer(String answer) {
		this.answer = answer;
	}
	public void setAnswerAr(String answerAr) {
		this.answerAr = answerAr;
	}

}
